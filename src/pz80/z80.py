    def __init__(self):
        """Z80クラスを初期化します。"""
        self._strmap = self._strmap_default
        if not Z80._initialized:
            Z80._build_maps()

    @classmethod
    def _build_maps(cls):
        """クラスレベルのマップ（予約語、命令マップなど）を構築します。"""
        # 予約語テーブルを生成
        r = set()

        # レジスター追加
        r.add("a")
        r.add("b")
        r.add("c")
        r.add("d")
        r.add("e")
        r.add("f")
        r.add("h")
        r.add("l")
        r.add("af")
        r.add("bc")
        r.add("de")
        r.add("sp")
        r.add("ix")
        r.add("iy")
        r.add("hl")
        r.add("ixl")
        r.add("ixh")
        r.add("iyl")
        r.add("iyh")

        # 条件指定追加
        r.add("z")
        r.add("p")
        r.add("m")
        r.add("nz")
        r.add("nc")
        r.add("po")
        r.add("pe")

        # オペコード追加
        for p in cls._codetbl:
            r.add(p["asm"][0])

        cls._reserved = list(r)
        cls._reserved.sort()

        # 高速検索用マップの生成
        cls._asm_map = {}
        cls._op_map = {}

        for item in cls._codetbl:
            # アセンブラ用マップ (ニーモニック -> 命令情報)
            asm_key = tuple(item["asm"])
            cls._asm_map[asm_key] = item

            # 逆アセンブラ用マップ (オペコード -> 命令情報)
            if item.get("ext") is not None:
                # DDCB/FDCB系: code (2bytes) + ext (1byte) をキーにする
                # 実際の命令列は [DD, CB, d, ext] だが、検索キーとしては (DD, CB, ext) を使用
                op_key = tuple(item["code"] + [item["ext"]])
            else:
                op_key = tuple(item["code"])
            cls._op_map[op_key] = item
        
        cls._initialized = True

    @property
    def codetbl(self):
        """Z80命令コードテーブルを取得します。"""
        return self._codetbl

    @property
    def reserved(self):
        """予約語リストを取得します。"""
        return self._reserved

    @property
    def asm_map(self):
        """アセンブラ用マップ（ニーモニック -> 命令情報）を取得します。"""
        return self._asm_map

    @property
    def op_map(self):
        """逆アセンブラ用マップ（オペコード -> 命令情報）を取得します。"""
        return self._op_map

    @property
    def strmap(self):
        """文字変換テーブルを取得します。"""
        return self._strmap

    @strmap.setter
    def strmap(self, p):
        """文字変換テーブルを設定します。"""
        self._strmap = p
